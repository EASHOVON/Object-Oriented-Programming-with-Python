""" 4-1 Simple Introduction To Function In Python And Return.
The def keyword is used to define a function and the return keyword is used to return something from the function. A few example of functions are given below - """

def add(num1, num2):
    return num1 + num2

def multiply(num1, num2):
    return num1 * num2

def substract(num1, num2):
    return num1 - num2

sum = add(35, 25)
product = multiply(35, 25)
difference = substract(product, sum)

print(difference)

